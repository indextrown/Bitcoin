# ==========================
# RSI 기반 자동매매 알고리즘
# ==========================
# [RSI(14) 60분봉]
# - RSI는 최근 상승/하락 강도를 0~100 범위로 나타내는 지표
# - RSI(14)는 최근 14개 캔들 기준
# - 60분봉 기준이면 최근 14시간 흐름을 본다는 의미
# - 일반적으로 RSI >= 70은 과매수, RSI <= 30은 과매도
# - 이 전략은 하락장에서 과매도 이후 반등 구간을 노린다
# - 상승장 대응은 약하므로 개선 여지가 있다

# ==========================
# [기준]
# ==========================
# - RSI(14), 60분봉 기준으로 판단
# - 봇은 15분마다 실행하므로 1시간에 4번 체크
# - 10분마다 실행하면 1시간에 6번 체크 가능
# - 60분봉 흐름은 유지하고, 15분 단위로 더 세밀하게 대응
# - 거래대금 상위 코인(top 10)만 매매 대상
# - 동시에 보유할 수 있는 코인은 최대 5개

# ==========================
# [신규 진입]
# ==========================
# 조건:
# - 이전 RSI <= 30
# - 현재 RSI > 30
# - 현재 보유 코인 수 < 5
#
# 실행:
# - 과매도 구간 이탈 시 코인당 최대 투자금의 일부만 첫 진입
# - BUY-NEW

# ==========================
# [물타기]
# ==========================
# 대상:
# - 이미 보유 중인 코인
#
# 조건:
# - 이전 RSI <= 30
# - 현재 RSI > 30
#
# 실행:
# - 코인당 투자금이 50% 이하이면 일반 물타기(BUY-WATER-1)
# - 50% 초과이면 수익률 -5% 이하일 때만 추가 물타기(BUY-WATER-2)
#
# 목적:
# - 무한 물타기 방지
# - 원금 소진 속도 조절

# ==========================
# [수익 실현 매도]
# ==========================
# 조건:
# - RSI >= 70
# - 수익률 >= 1%
#
# 실행:
# - 현재 매수금액이 코인당 최대 투자금의 25% 미만이면 전량 매도
# - 현재 매수금액이 코인당 최대 투자금의 25% 이상이면 절반 매도
#
# 목적:
# - 과매수 구간에서 분할 익절
# - 되돌림 위험 축소

# ==========================
# [손절]
# ==========================
# 조건:
# - 원화 잔고 부족
# - 수익률 <= -10%
#
# 실행:
# - 절반 손절(SELL-LOSS-HALF)
#
# 목적:
# - 원금 보호
# - 추가 물타기 불가 상황 대비

# ==========================
# [전체 전략 요약]
# ==========================
# - 거래량 높은 코인(top 10)만 대상으로 본다
# - RSI가 30 아래에서 30 위로 올라올 때 분할 매수한다
# - 이미 보유 중이면 비중과 수익률에 따라 제한적으로 물타기한다
# - RSI가 70 이상이고 수익률이 1% 이상이면 분할 익절한다
# - 원화가 부족하고 수익률이 -10% 이하이면 절반 손절한다
